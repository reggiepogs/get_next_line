#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int is_neg;

	i = 0;
	n = 0;
	is_neg = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		is_neg = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while ((str[i] >= '0' && str[i] <= '9') && str[i])
	{
		n *= 10;
		n += (int)str[i] - '0';
		i++;
	}
	return (n * is_neg);
}
