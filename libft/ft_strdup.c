#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s1_len;
	char	*str;

	i = 0;
	s1_len = ft_strlen(s1);
	str = (char *)malloc(sizeof(char ) * (s1_len + 1));
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
